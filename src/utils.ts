export function createUserObj(fio: string, email: string, password: string, bankAccount_id: string): IUser {
    return {
        _id: new ObjectId().toString(),
        fio: fio,
        email: email,
        password: password,
        status: "Authorized",
        currentBankAccount: bankAccount_id,
        twoStepAuth: false
    };
}

export function createBankAccountObj(userId: string, bankAccount_id: string, name?: string, currency?: string, balance?: number): IBankAccount {
    return {
        _id: bankAccount_id,
        user_id: userId,
        name: name ?? "Счёт",
        currency: currency ?? "BYN",
        balance: balance ?? 0,
        invitingCode: inviteCode(),
        incomeCategories: [],
        expensesCategories: []
    };
}

export function inviteCode() {
    const characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789!@#$%^&*()_+';
    let code = '';
    for (let i = 0; i < 16; i++) {
        code += characters.charAt(Math.floor(Math.random() * characters.length));
    }
    return code;
}

export function generate2FAcode() {
    const characters = '0123456789';
    let password = '';
    for (let i = 0; i < 6; i++) {
        password += characters.charAt(Math.floor(Math.random() * characters.length));
    }
    return password;
}

export function generatePassword() {
    const characters = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789!@#$%^&*()_+';
    let password = '';
    for (let i = 0; i < 12; i++) {
        password += characters.charAt(Math.floor(Math.random() * characters.length));
    }
    return password;
}

